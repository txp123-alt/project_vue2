<template>
  <div class="type-nav">
    <div class="container">
      <div @mouseleave="leaveShow" @mouseenter="enterShow">
        <h2 class="all">全部商品分类</h2>
        <!--添加过渡动画，前提是必须有 v-show | v-if -->
        <transition name="sort">
          <div class="sort" v-show="show">
            <!--一级分类-->
            <div class="all-sort-list2" @click="goSearch">
              <div class="item" v-for="(c1,index) in categoryList" :key="c1.categoryId" :class="{cur:currentIndex===index}">
                <h3 @mouseenter="changeIndex(index)">
                  <a :data-categoryName="c1.categoryName" :data-category1Id="c1.categoryId">{{c1.categoryName}}</a>
                </h3>
                <!--二级,三级分类-->
                <div class="item-list clearfix" :style="{display : currentIndex === index ? 'block' : 'none'}">
                  <div class="subitem" v-for="(c2,index) in c1.categoryChild" :key="c2.categoryId">
                    <dl class="fore">
                      <dt>
                        <a :data-categoryName="c2.categoryName" :data-category2Id="c2.categoryId">{{c2.categoryName}}</a>
                      </dt>
                      <dd>
                        <em v-for="(c3,index) in c2.categoryChild" :key="c3.categoryId">
                          <a :data-categoryName="c3.categoryName" :data-category3Id="c3.categoryId">{{c3.categoryName}}</a>
                        </em>
                      </dd>
                    </dl>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </transition>
      </div>
      <nav class="nav">
        <a href="###">服装城</a>
        <a href="###">美妆馆</a>
        <a href="###">尚品汇超市</a>
        <a href="###">全球购</a>
        <a href="###">闪购</a>
        <a href="###">团购</a>
        <a href="###">有趣</a>
        <a href="###">秒杀</a>
      </nav>
    </div>
  </div>
</template>
<script>
import {mapState} from "vuex";
import _ from "lodash"; //防抖与节流

export default {
  name:'TypeNav',
  computed:{
    ...mapState({
      //这个函数会在页面使用这个计算属性时触发，从state中拿到指定数据进行赋值
      categoryList:(state)=>{
        return state.home.categoryList;
      }
    })
  },
  data(){
    return {
      //保存当前用户鼠标移动到哪个一级分类上
      currentIndex: -1,
      show: true
    }
  },
  mounted() {
    //组件挂载完毕后，请求数据, 将请求到数据的保存到store中
    this.$store.dispatch('categoryList')
    //判断当前路由是否处于search, 如果处于search路由下，那么隐藏三级联动
    if (this.$route.path !== '/home'){
      this.show = false
    }
  },
  methods:{
    //一级标签切换时添加节流动作
    changeIndex:_.throttle(function (index){
      this.currentIndex = index
    },50),
    leaveShow(){
      this.currentIndex = -1
      if (this.$route.path !== '/home'){
        this.show = false
      }
    },
    //适配search页, 鼠标移入时, 展示三级联动
    enterShow(){
      this.show = true
    },
    //点击三级联动标签，跳转到search组件(事件委派）
    goSearch(event){
      //获取触发当前事件的元素
      let eventment = event.target
      //获取这个元素上的所有属性,并且结构赋值，取出categoryname属性
      let {categoryname,category1id,category2id,category3id} = eventment.dataset;
      console.log("@goSearch",categoryname)
      if (categoryname){
        //创建参数
        let location = {name:'search'}
        let query = {'categoryName' : categoryname}
        if (category1id){
          query.category1Id = category1id
        }else if (category2id){
          query.category2Id = category2id
        }else if (category3id){
          query.category3Id = category3id
        }
        //路由跳转
        location.query = query
        this.$router.push(location)
      }
    },
  },
}
</script>
